<h2>Nudel mit Rosmarin (4 Personen)</h2>
Veggie Hack 100g in 0,5 liter Suppe (ganzer Suppenwürfel) sobald aufkocht, dann 10 Minuten köcheln.<br>
Knoblauch 8 Zehen, Rosmarin, Salz, Pfeffer, 100ml Olivenöl, Zwiebel anrösten, Veggie Hack dazu und eine Dose Tomatensoße.<br>
Je nach Wunsch 500g Nudel, Penne, Spagetti, ... mit Salz für ca. 8-10 Minuten kochen.<br>
Dazu frischen grünen Salat.</br></br>

<h2>Palatschinken</h2>
500ml Milch bzw. Wasser und nur einen Teil Milch</br>
200g Mehl</br>
Eine Priese Salz</br></br>
Zubereitung: </br>
Alles Verrühren und dann die Eier dazu geben und weiter verrühren.Mit 4 Eier wird es sehr fluffich und fragil aber lecker!
Sonst laut Rezept sind es 6 Eier...</br>
Rezept für ca. 4 Personen genau passend.
Nutella, Marmelade, Eis, Bananen oder andere Früchte zugeben.
</br></br>

<h2>Pizzateig (4 Personen)</h2>
500g Weizenmehl Type 405</br>
300ml Wasser</br>
2 TL Salz</br>
2 Packungen (zu 7g) Frischhefe</br>
1/2 TL Zucker</br>
7 EL natives Olivenöl
</br></br>
Zubereitung: </br>
Zunächst sieben wir das Mehl mit dem Salz und füllen verrühren. Nun wird die Hefe in dem lauwarmen Wasser aufgelöst und anschließend zum Mehl/Salz-Gemisch gegeben.
Anschließend werden die Zutaten zu einem elastischen, homogenen Teig verknetet. Nach dem Kneten wird der Teig mit einem feuchten Tuch abgedeckt und darf ca zwei 
Stunden ruhen. Für die Tomatensoße einfach eine Dose Tomaten mit (viel) Knoblauch, Salz, Pfeffer, etwas Zucker, Basilikum und Oregano kalt pürieren, 
nicht kochen.</br>
<br/>


<h2>Schwammerl mit Lungenbraten Tascherl</h2>
200g Champions</br>
150g Lungenbraten pro Person</br>
300ml Zwiebel</br></br>
Zubereitung: </br>
Schneiden der Champions und der Zwiebel, Zuerst Zwiebel anbraten dann dünsten (Flüssigkeit) der Champions. Hinzugeben von Zitronen Saft, Salz, Pfeffer und wenig Ketchup. Lungebraten Stücke (ca. 1 cm) anbraten und im Blätterteug im Rohr Backen. Zusammen servieren.
</br></br>


<h2>Walnussbrot</h2>
250g Weizenmehl<br/>
300g Roggenmehrl<br/>
2 EL Honig<br/>
2 TL Salz<br/>
1 Pck. Trockenhefe<br/>
150g Walnusskerne<br/><br/>
Zubereitung:<br/>
Backen: 30 Min.<br/>
Ruhen: 90 Min.<br/><br/>
1. Schritt<br/>
Mehle mit Honig, Salz und Trockenhefe in einer Schüssel mischen. 370 ml lauwarmes Wasser zugeben und alles mit den
Knethaken des Handrührgeräts 5 Min. zu einem glatten Teig kneten. Walnusskerne grob hacken und unter den Teig kneten. Zugedeckt ca. 60 Min. gehen lassen.<br/>
2. SCHRITT<br/>
Teig auf einer leicht bemehlten Arbeitsfläche durchkneten. Teig halbieren. Jede Hälfte zu einem ca. 30 cm langen Strang
formen. Auf ein mit Backpapier belegtes Blech setzen und zugedeckt ca. 30 Min. ruhen lassen.<br/>
3. SCHRITT<br/>
Backofen auf 220 Grad (Umluft: 200 Grad) vorheizen. Baguettes 2-3 mal leicht einschneiden. Auflaufform mit
Wasser auf den Boden des Backofens stellen. Baguettes 10 Min. im Backofen anbacken. Danach die Ofentemperatur auf
200 Grad (Umluft: 180 Grad) reduzieren und die Baguettes 20 Min. fertig backen. Vollständig erkalten lassen.
